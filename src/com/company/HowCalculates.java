package com.company;

public class HowCalculates {
    public static void main(String[] args) {
    int n=5/3;
    System.out.println("n="+n);
    double m=5.0/0*0;
    System.out.println("m="+m);
    m=Math.sin(Math.PI/6);
    System.out.println("m="+m);
    n=6/2;
    System.out.println("n="+n);
    float n2=6.0f;
    System.out.println(n2);
}
}
